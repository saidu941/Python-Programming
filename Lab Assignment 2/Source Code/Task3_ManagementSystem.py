#1.Student Class
class Student:
    'common base class for all the students'
    stuCount=0

    def __init__(self,name,age,sex):
        self.name=name
        self.age=age
        self.sex=sex

    def displayCount(self):
        print('total number of students is: ',Student.stuCount)

    def displayInfo(self):
        print('name is : ',self.name)
        print('age is : ',self.age)
        print('gender is : ',self.sex)

student1=Student('saidu','23','M')
print(student1.stuCount)
student1.displayCount()
student1.displayInfo()




